// RFC-092 §5-8 — S-1 端到端验收（review iterate 形态，修复后正确语义锁定）。
//
// 锁定 design/RFC-092-scheduler-p0-stopgap/design.md §1.2 的 pending 锚点行
// 一次性豁免在【真实 runTask + 真实 submitReviewDecision 入口】下的端到端
// 效果。对应调研报告 design/scheduler-audit-2026-06-10.md S-1：review
// iterate/reject（review.ts）给本调用已派发过的节点铸 pending rerun 行 +
// 把 review 行原地翻回 pending；修复前这两个 pending 行都被
// `dispatchedThisInvocation` 永久屏蔽，sibling 收尾后任务以
// `'scheduler stalled'` 假失败收场。修复后正确语义：
//
//   in → {author → review, slow}。review 停泊 awaiting_review；slow 仍在跑时
//   用户经 submitReviewDecision(iterated)（真实服务入口）打回 → 活调度循环在
//   slow settle 后的 tick 经 pending-bypass 依次自取：author 的 rerun 行
//   （retryIndex+1 新铸行）先跑 done，review 行（原行翻 pending、
//   reviewIteration+1）随后重派、铸 v2 doc_version 再次停泊 → 任务以
//   awaiting_review（真实状态）收场而非 stalled。随后 approve + resume
//   把任务推到 done，确认全链路推进。
//
// 时序确定性：slow 走「gate 文件」慢 mock（同 rfc092-midrun-clarify-dispatch
// .test.ts；窗口由测试显式关闭，不赌固定 sleep）；测试侧一律轮询具体 DB
// 状态。prompt 证据走 mock 落盘 argv（scheduler-audit-s05 的
// MOCK_OPENCODE_CAPTURE_ARGV_TO 模式：argv[1] 即完整 user prompt）。

