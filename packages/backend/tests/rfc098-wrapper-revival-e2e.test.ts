// RFC-098 B3（audit S-3 + RFC-092 已知限制解除）端到端验收 — 修复后正确语义锁定。
//
// 三个场景（修复前全部 RED）：
//
//   1. approve-inside-loop：loop ∋ {designer, review}。review approve 后只
//      resume 一次任务即自动推进到 done。修复前 approve 不铸任何 pending 行，
//      wrapper awaiting_review 行的唯一放行通道 wrapperHasFreshInnerWork 只认
//      pending → 无论 resume 多少次都弹回 awaiting_review（双 resume 也救不
//      回）。修复后 wrapperRevivalEvidence 认「窗口内 review done∧fresh 行」
//      为复活证据。
//
//   2. approve-inside-git：wrapper-git 同型 + git_diff 恰写一次。
//
//   3. mid-run：菱形 in → {loop ∋ {asker, clarify}, slow}。slow sibling 仍在
//      跑时答 clarify → 活调度循环自取 → 任务自动 done，**不需要手动 resume**。
//      这正是 design/RFC-092-…/design.md §「已知限制」记录的形态：bypass 只
//      豁免 pending 行不豁免 wrapper awaiting_* 行 → 修复前任务停泊
//      awaiting_human（inbox 已空）等人工 resume。修复 = deriveFrontier 的
//      wrapperAnchorReleasable（证据行 id 一次性豁免，scheduler.ts）。
//
// 时序确定性：gate 文件慢 mock（同 rfc092-midrun-clarify-dispatch.test.ts），
// 测试侧轮询 DB 状态，绝不裸 sleep 赌时机。

