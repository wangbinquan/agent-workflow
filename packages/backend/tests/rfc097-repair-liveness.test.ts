const ulid = monotonicFactory()
const MIGRATIONS = resolve(import.meta.dir, '..', 'db', 'migrations')

const SCHEDULER_ACTIVE_KEY = 'diagnose.repair.common.schedulerActive'

// ---------------------------------------------------------------------------
// gate-held mock opencode: blocks until the gate file appears, then emits a
// normal <workflow-output> envelope. Gate path baked into the generated
// source (zero process.env dependence → no cross-test interference).
// ---------------------------------------------------------------------------
function gateMockSource(gateFile: string): string {
  return `// generated by rfc097-repair-liveness.test.ts — gate-held mock opencode
import { existsSync } from 'node:fs'
const argv = process.argv.slice(2)
if (argv.includes('--version')) { process.stdout.write('rfc097-gate-mock 1.14.99\\n'); process.exit(0) }
if (argv[0] !== 'run') { process.stderr.write('rfc097-gate-mock: expected run\\n'); process.exit(2) }
const deadline = Date.now() + 20000
while (!existsSync(${JSON.stringify(gateFile)})) {
  if (Date.now() > deadline) { process.stderr.write('rfc097-gate-mock: gate timeout\\n'); process.exit(1) }
  await Bun.sleep(25)
}
const envelope = '<workflow-output>\\n  <port name="out">ok</port>\\n</workflow-output>'
process.stdout.write(JSON.stringify({ type: 'text', timestamp: Date.now(), part: { type: 'text', text: envelope } }) + '\\n')
process.exit(0)
`
}

interface LiveHarness {
  db: DbClient
  appHome: string
  gateFile: string
  mockPath: string
  cleanup: () => void
}

async function buildLiveHarness(): Promise<LiveHarness> {
  const appHome = mkdtempSync(join(tmpdir(), 'aw-rfc097-liveness-'))
  const repoPath = join(appHome, 'repo')
  const worktreePath = join(appHome, 'wt')
  const gateFile = join(appHome, 'gate')
  const mockPath = join(appHome, 'gate-mock.ts')
  mkdirSync(repoPath, { recursive: true })
  mkdirSync(worktreePath, { recursive: true })
  for (const p of [repoPath, worktreePath]) {
    await runGit(p, ['init', '-b', 'main'])
    await runGit(p, ['config', 'user.email', 't@t.test'])
    await runGit(p, ['config', 'user.name', 't'])
    writeFileSync(join(p, 'r.md'), '# r\n')
    await runGit(p, ['add', '.'])
    await runGit(p, ['commit', '-m', 'init'])
  }
  writeFileSync(mockPath, gateMockSource(gateFile))
  const db = createInMemoryDb(MIGRATIONS)
  return {
    db,
    appHome,
    gateFile,
    mockPath,
    cleanup: () => rimrafDir(appHome),
  }
}

/** Minimal input → gated-agent workflow: one spawn, parked on the gate. */
function gatedDef(): WorkflowDefinition {
  return {
    $schema_version: 3,
    inputs: [{ kind: 'text', key: 'req', label: 'r' }],
    nodes: [
      { id: 'in1', kind: 'input', inputKey: 'req' } as WorkflowNode,
      { id: 'slow', kind: 'agent-single', agentName: 'slow' } as WorkflowNode,
    ],
    edges: [
      {
        id: 'e1',
        source: { nodeId: 'in1', portName: 'req' },
        target: { nodeId: 'slow', portName: 'req' },
      },
    ],
  }
}

async function seedInterruptedTask(h: LiveHarness): Promise<string> {
  await h.db.insert(agents).values({
    id: ulid(),
    name: 'slow',
    description: 'test',
    outputs: JSON.stringify(['out']),
    permission: '{}',
    skills: '[]',
    frontmatterExtra: '{}',
    bodyMd: '',
  })
  const def = gatedDef()
  const workflowId = ulid()
  const taskId = ulid()
  await h.db.insert(workflows).values({
    id: workflowId,
    name: 'wf-rfc097-liveness',
    definition: JSON.stringify(def),
  })
  await h.db.insert(tasks).values({
    id: taskId,
    name: 'rfc097-liveness',
    workflowId,
    workflowSnapshot: JSON.stringify(def),
    repoPath: join(h.appHome, 'repo'),
    worktreePath: join(h.appHome, 'wt'),
    baseBranch: 'main',
    branch: `agent-workflow/${taskId}`,
    status: 'interrupted',
    inputs: JSON.stringify({ req: 'go' }),
    startedAt: Date.now(),
  })
  return taskId
}

// Bounded polling (25ms) — never bet on a fixed sleep.
async function waitFor<T>(
  probe: () => Promise<T | undefined>,
  what: string,
  timeoutMs = 10_000,
): Promise<T> {
  const deadline = Date.now() + timeoutMs
  for (;;) {
    const v = await probe()
    if (v !== undefined) return v
    if (Date.now() > deadline) throw new Error(`timed out waiting for ${what}`)
    await Bun.sleep(25)
  }
}

async function taskStatusOf(db: DbClient, taskId: string): Promise<string> {
  const rows = await db
    .select({ status: tasks.status })
    .from(tasks)
    .where(eq(tasks.id, taskId))
    .limit(1)
  return rows[0]?.status ?? '<gone>'
}

describe('RFC-097 S-23 — repair preflight refuses while a live scheduler owns the task', () => {
  describe('real runTask attached (gate-held agent)', () => {
    let h: LiveHarness
    beforeEach(async () => {
      h = await buildLiveHarness()
    })
    afterEach(async () => {
      // Open the gate + abort so a failing assertion can't leak a parked
      // scheduler (and its bun child) past the test.
      try {
        writeFileSync(h.gateFile, 'release')
      } catch {
        /* gate dir already removed */
      }
      if (abortAllActiveTasks().length > 0) await Bun.sleep(100)
      h.cleanup()
    })

    test('list → all S3 options schedulerActive; apply → 409 repair-preflight-stale; gate release frees the task', async () => {
      const taskId = await seedInterruptedTask(h)
      const deps = { db: h.db, appHome: h.appHome, opencodeCmd: ['bun', 'run', h.mockPath] }

      // Real ownership path: resumeTask registers the AbortController, then
      // its kicked runTask CASes pending→running and parks on the gate.
      await resumeTask(h.db, taskId, deps)
      await waitFor(
        async () => ((await taskStatusOf(h.db, taskId)) === 'running' ? true : undefined),
        'task to reach running under the live scheduler',
      )
      expect(isTaskActive(taskId)).toBe(true)

      const alertId = await insertAlert(h.db, taskId, { rule: 'S3', detail: {} })
      const listed = await listRepairOptionsForAlert({
        db: h.db,
        taskId,
        alertId,
        actorUserId: null,
        appHome: h.appHome,
        deps,
      })
      expect(listed.options.length).toBe(4)
      for (const o of listed.options) {
        expect({ id: o.id, available: o.available, reason: o.unavailableReasonKey }).toEqual({
          id: o.id,
          available: false,
          reason: SCHEDULER_ACTIVE_KEY,
        })
      }

      // Apply re-runs preflight → unavailable → existing preflight-stale
      // contract (409 + audit row), with zero writes to the live task.
      let code: string | undefined
      try {
        await applyRepairOption({
          db: h.db,
          taskId,
          alertId,
          optionId: 'S3.demote-task',
          actorUserId: null,
          appHome: h.appHome,
          deps,
        })
      } catch (err) {
        code = (err as { code?: string }).code
      }
      expect(code).toBe('repair-preflight-stale')
      expect(await taskStatusOf(h.db, taskId)).toBe('running')
      const audits = await readAuditRows(h.db, taskId)
      expect(audits.map((a) => a.outcome)).toEqual(['preflight-stale'])

      // Release the gate: the agent finishes, the scheduler detaches, and
      // the gate must release with it (reason flips to the plain status
      // gate — schedulerActive must not stick to a detached task).
      writeFileSync(h.gateFile, 'release')
      await waitFor(
        async () => ((await taskStatusOf(h.db, taskId)) === 'done' ? true : undefined),
        'task to finish after gate release',
      )
      await waitFor(
        async () => (isTaskActive(taskId) ? undefined : true),
        'scheduler to detach from the task',
      )
      const relisted = await listRepairOptionsForAlert({
        db: h.db,
        taskId,
        alertId,
        actorUserId: null,
        appHome: h.appHome,
        deps,
      })
      const demote = relisted.options.find((o) => o.id === 'S3.demote-task')
      expect(demote?.available).toBe(false)
      expect(demote?.unavailableReasonKey).toBe('diagnose.repair.S3.unavailable.taskNotRunning')
    }, 30_000)
  })

  describe('negative control: no scheduler attached', () => {
    let h: RepairHarness
    afterEach(() => h.cleanup())

    test('running task without a live scheduler is NOT gated (liveness-keyed, not status-keyed)', async () => {
      h = await buildRepairHarness({ taskStatus: 'running' })
      const alertId = await insertAlert(h.db, h.taskId, { rule: 'S3', detail: {} })
      const listed = await listRepairOptionsForAlert({
        db: h.db,
        taskId: h.taskId,
        alertId,
        actorUserId: null,
        appHome: h.tmpDir,
        deps: h.deps,
      })
      const demote = listed.options.find((o) => o.id === 'S3.demote-task')
      const markFailed = listed.options.find((o) => o.id === 'S3.mark-task-failed')
      expect(demote?.available).toBe(true)
      expect(markFailed?.available).toBe(true)
      for (const o of listed.options) {
        expect(o.unavailableReasonKey).not.toBe(SCHEDULER_ACTIVE_KEY)
      }
    })
  })
})
