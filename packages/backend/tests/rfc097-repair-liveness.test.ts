import { rimrafDir } from './helpers/cleanup'
// RFC-097 (audit S-23) — lifecycleRepair scheduler-liveness preflight.
//
// Locks in: repair options that flip tasks.status and/or resumeAfterApply must
// refuse (`unavailable: diagnose.repair.common.schedulerActive`) while an
// in-process scheduler loop owns the task (task.ts activeTasks). Before this
// gate, an operator could demote/fail/cancel a task the live runTask was still
// driving — kicking a second scheduler under the first or clobbering its CAS
// writes (the S-8 double-driver family, surfaced through the repair UI).
//
// Liveness is exercised through the REAL path: resumeTask registers the
// AbortController and kicks runTask against a gate-held mock opencode (the
// agent blocks until the test writes the gate file — no sleeps-as-timing, same
// pattern as rfc092-midrun-clarify-dispatch.test.ts). The harness-built tasks
// used by every other lifecycle-repair-*.test.ts never enter activeTasks, so
// those suites double as the gate's negative control; test 2 locks that
// explicitly.

