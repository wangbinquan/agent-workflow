// RFC-095 — wrapper canceled 复活：续跑而非重启（audit S-22 连带决策）。
// design/RFC-095-scope-outcome-exhaustive/design.md §1（爆炸半径核查第 3 条）/ §5-6。
//
// 锁定的三件套（任一回归即红）：
//   1. isDispatchable(canceled 无标记)=true —— 取消任务复活后，top-level wrapper 的
//      canceled 行重新进入 ready（dispatchFrontier.ts）。
//   2. findResumableWrapperRun 不再把 canceled 列为 terminal —— 复活走「同一行 id
//      复用 + 持久化 progress 续跑」，而不是铸新 wrapper 行从 iteration 0 重走
//      （旧行为会让 loop 重绕、git 重取错误 baseline）。
//   3. wrapper-resume 的 setNodeRunStatus allowedFrom 含 'canceled'（allowTerminal）
//      —— canceled→running 合法，与 interrupted 续跑语义对齐
//      （先例：scheduler-boundary-wrapper-resume-interrupted.test.ts）。
//
// 场景（loop 路线；取消时序确定性构造）：wrapper-loop(maxIterations=3, 退出条件
// port-equals inner1.out=='EXIT')。自写 invocation-counter mock：第 1 次调用（迭代 0）
// 输出 CONTINUE；第 2 次调用（迭代 1）先写 marker 文件再阻塞——测试等到 marker 出现后
// controller.abort()（与 cancelTask 同一机制：共享 AbortSignal → runner SIGTERM 子进程
// → inner 行翻 canceled → wrapper 行经 markWrapperTerminal 翻 canceled，progress 已在
// 迭代 1 开头持久化）；第 3 次调用（复活重跑迭代 1）输出 EXIT。全程无 sleep 竞态：
// 每一步都由文件 marker / DB 终态轮询闸出。

