// RFC-092 §5-7 — S-1 端到端验收（修复后正确语义锁定）。
//
// 锁定 design/RFC-092-scheduler-p0-stopgap/design.md §1.2 的 pending 锚点行
// 一次性豁免（deriveFrontier 的 pendingAnchorReleasable + runScope 的
// dispatchedPendingRowIds）在【真实 runTask + 真实 autoDispatchClarifyRound 入口】
// 下的端到端效果。对应调研报告 design/scheduler-audit-2026-06-10.md S-1：
// 修复前，mid-run 答题铸出的 pending rerun 行被 `dispatchedThisInvocation`
// 永久屏蔽，sibling 收尾后 runScope 静默块四个出口全不命中，任务以
// `'scheduler stalled'` 假失败收场。修复后正确语义：
//
//   菱形 in → {asker, slow}。asker 发 <workflow-clarify> 停泊；slow 仍在跑时
//   用户经 autoDispatchClarifyRound（真实服务入口，seal 双表镜像由它自己完成）
//   答题 → 活调度循环在下一 tick（slow settle 触发）经 pending-bypass 自取
//   rerun 行 → asker 带 Q&A 重跑 done → 任务 done。slow 恰好跑一次、不受扰。
//
// 时序确定性：slow 走「gate 文件」慢 mock（轮询 gate 出现才放行，等价于
// fixtures/mock-opencode.ts 的 MOCK_OPENCODE_DELAY_MS 但窗口由测试显式关闭，
// 不赌固定 sleep）；测试侧一律轮询具体 DB 状态（参考
// scheduler-clarify-dispatch.test.ts 的等待模式）。prompt 证据走 mock 落盘
// argv（参考 scheduler-audit-s05 的 MOCK_OPENCODE_CAPTURE_ARGV_TO 模式：
// argv[1] 即渲染后的完整 user prompt）。

