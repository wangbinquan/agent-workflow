async function seedWorkflowAndTask(
  h: Harness,
  definition: WorkflowDefinition,
  inputs: Record<string, string>,
): Promise<string> {
  const workflowId = ulid()
  const taskId = ulid()
  await h.db.insert(workflows).values({
    id: workflowId,
    name: 'wf-rfc092-s1',
    definition: JSON.stringify(definition),
  })
  await h.db.insert(tasks).values({
    name: 'rfc092-midrun-clarify',
    id: taskId,
    workflowId,
    workflowSnapshot: JSON.stringify(definition),
    repoPath: h.repoPath,
    worktreePath: h.worktreePath,
    baseBranch: 'main',
    branch: `agent-workflow/${taskId}`,
    status: 'pending',
    inputs: JSON.stringify(inputs),
    startedAt: Date.now(),
  })
  return taskId
}

// 有限次轮询等待（25ms 间隔），等不到抛错 —— 绝不裸 sleep 赌时机。
async function waitFor<T>(
  probe: () => Promise<T | undefined>,
  what: string,
  timeoutMs = 10_000,
): Promise<T> {
  const deadline = Date.now() + timeoutMs
  for (;;) {
    const v = await probe()
    if (v !== undefined) return v
    if (Date.now() > deadline) throw new Error(`timed out waiting for ${what}`)
    await Bun.sleep(25)
  }
}

async function topLevelRuns(db: DbClient, taskId: string, nodeId: string) {
  const rows = await db
    .select()
    .from(nodeRuns)
    .where(and(eq(nodeRuns.taskId, taskId), eq(nodeRuns.nodeId, nodeId)))
  return rows.filter((r) => r.parentNodeRunId === null)
}

function readSpawns(stateDir: string): Array<{ agent: string; callIndex: number; prompt: string }> {
  const raw = readFileSync(join(stateDir, 'argv.jsonl'), 'utf-8')
  return raw
    .split('\n')
    .filter((l) => l.trim().length > 0)
    .map((l) => {
      const row = JSON.parse(l) as { agent: string; callIndex: number; argv: string[] }
      // runner 以 `run "<prompt>" --agent NAME ...` 调起 mock；argv 已 slice(2)，
      // 所以 argv[1] 即渲染后的完整 user prompt（同 scheduler-audit-s05）。
      return { agent: row.agent, callIndex: row.callIndex, prompt: row.argv[1] ?? '' }
    })
}

