// ---------------------------------------------------------------------------
// gate 慢 mock：plan/state/gate 路径全部在生成时烘焙进脚本（零 process.env
// 依赖，杜绝并发测试间串扰）。带 gate 的 agent 在发 envelope 前轮询 gate 文件
// 出现（25ms 间隔、20s 上限兜底，超时 exit 1 让失败显形）；其余 agent 按
// per-agent 调用计数走 plan（同 fixtures/scenario-opencode.ts 的步进语义：
// 越界重复最后一步）。每次被调起 append 一行 {agent, callIndex, argv} 到
// state 目录的 argv.jsonl。
// ---------------------------------------------------------------------------
function gateMockSource(paths: {
  planFile: string
  stateDir: string
  gates: Record<string, string>
}): string {
  return `// generated by rfc092-midrun-clarify-dispatch.test.ts — gate-driven mock opencode
import { appendFileSync, existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'
const PLAN_FILE = ${JSON.stringify(paths.planFile)}
const STATE_DIR = ${JSON.stringify(paths.stateDir)}
const GATES: Record<string, string> = ${JSON.stringify(paths.gates)}
const argv = process.argv.slice(2)
if (argv.includes('--version')) { process.stdout.write('midrun-gate-mock 1.14.99\\n'); process.exit(0) }
if (argv[0] !== 'run') { process.stderr.write('midrun-gate-mock: expected run\\n'); process.exit(2) }
const nonce = /\\bnonce="([^"]+)"/.exec(argv[1] ?? '')?.[1]
const outputOpen = nonce === undefined ? '<workflow-output>' : '<workflow-output nonce="' + nonce + '">'
const clarifyOpen = nonce === undefined ? '<workflow-clarify>' : '<workflow-clarify nonce="' + nonce + '">'
const ai = argv.indexOf('--agent')
const agent = ai >= 0 ? (argv[ai + 1] ?? '') : ''
if (agent === '') { process.stderr.write('midrun-gate-mock: missing --agent\\n'); process.exit(2) }
mkdirSync(STATE_DIR, { recursive: true })
const counterFile = join(STATE_DIR, 'count-' + agent)
let n = 0
if (existsSync(counterFile)) n = Number(readFileSync(counterFile, 'utf-8').trim()) || 0
const callIndex = n
writeFileSync(counterFile, String(n + 1))
appendFileSync(join(STATE_DIR, 'argv.jsonl'), JSON.stringify({ agent, callIndex, argv }) + '\\n')
const gate = GATES[agent]
if (gate !== undefined) {
  const deadline = Date.now() + 20000
  while (!existsSync(gate)) {
    if (Date.now() > deadline) { process.stderr.write('midrun-gate-mock: gate timeout\\n'); process.exit(1) }
    await Bun.sleep(25)
  }
}
const plan = JSON.parse(readFileSync(PLAN_FILE, 'utf-8')) as Record<string, Array<Record<string, unknown>>>
const steps = plan[agent] ?? []
const step = steps.length > 0 ? steps[Math.min(callIndex, steps.length - 1)]! : { output: { out: 'default-' + agent } }
function emit(text: string): void {
  process.stdout.write(JSON.stringify({ type: 'text', timestamp: Date.now(), part: { type: 'text', text } }) + '\\n')
}
if (step.clarify !== undefined) {
  emit(clarifyOpen + JSON.stringify(step.clarify) + '</workflow-clarify>')
  process.exit(0)
}
const outputs = (step.output ?? {}) as Record<string, string>
let envelope = outputOpen + '\\n'
for (const [p, c] of Object.entries(outputs)) envelope += '  <port name="' + p + '">' + c + '</port>\\n'
envelope += '</workflow-output>'
emit(envelope)
process.exit(0)
`
}

interface Harness {
  db: DbClient
  appHome: string
  repoPath: string
  worktreePath: string
  stateDir: string
  planFile: string
  gateFile: string
  mockPath: string
  cleanup: () => void
}

async function buildHarness(): Promise<Harness> {
  const appHome = mkdtempSync(join(tmpdir(), 'aw-rfc092-clarify-'))
  const repoPath = join(appHome, 'repo')
  const worktreePath = join(appHome, 'wt')
  const stateDir = join(appHome, 'mock-state')
  const planFile = join(appHome, 'plan.json')
  const gateFile = join(appHome, 'slow-gate')
  const mockPath = join(appHome, 'gate-mock.ts')
  mkdirSync(repoPath, { recursive: true })
  mkdirSync(worktreePath, { recursive: true })
  mkdirSync(stateDir, { recursive: true })
  // 真实 git：调度器要对 worktree 取 pre_snapshot，autoDispatchClarifyRound 的
  // self 回滚也要在真实仓上执行（同 scheduler-clarify-dispatch.test.ts）。
  await runGit(repoPath, ['init', '-b', 'main'])
  await runGit(repoPath, ['config', 'user.email', 't@t.test'])
  await runGit(repoPath, ['config', 'user.name', 't'])
  writeFileSync(join(repoPath, 'README.md'), '# r\n')
  await runGit(repoPath, ['add', '.'])
  await runGit(repoPath, ['commit', '-m', 'init'])
  await runGit(worktreePath, ['init', '-b', 'main'])
  await runGit(worktreePath, ['config', 'user.email', 't@t.test'])
  await runGit(worktreePath, ['config', 'user.name', 't'])
  writeFileSync(join(worktreePath, 'r.md'), '# r\n')
  await runGit(worktreePath, ['add', '.'])
  await runGit(worktreePath, ['commit', '-m', 'init'])
  writeFileSync(mockPath, gateMockSource({ planFile, stateDir, gates: { slow: gateFile } }))
  const db = createInMemoryDb(MIGRATIONS)
  return {
    db,
    appHome,
    repoPath,
    worktreePath,
    stateDir,
    planFile,
    gateFile,
    mockPath,
    cleanup: () => rmSync(appHome, { recursive: true, force: true }),
  }
}

