// 菱形 in → {asker, slow}；asker 挂 clarify 节点（问/答双通道边）。
function diamondDef(): WorkflowDefinition {
  return {
    $schema_version: 3,
    inputs: [{ kind: 'text', key: 'req', label: 'r' }],
    nodes: [
      { id: 'in1', kind: 'input', inputKey: 'req' } as WorkflowNode,
      { id: 'asker', kind: 'agent-single', agentName: 'asker' } as WorkflowNode,
      { id: 'c', kind: 'clarify', title: 'Clarify me' } as WorkflowNode,
      { id: 'slow', kind: 'agent-single', agentName: 'slow' } as WorkflowNode,
    ],
    edges: [
      {
        id: 'e_in_a',
        source: { nodeId: 'in1', portName: 'req' },
        target: { nodeId: 'asker', portName: 'req' },
      },
      {
        id: 'e_in_s',
        source: { nodeId: 'in1', portName: 'req' },
        target: { nodeId: 'slow', portName: 'req' },
      },
      {
        id: 'e_ask',
        source: { nodeId: 'asker', portName: '__clarify__' },
        target: { nodeId: 'c', portName: 'questions' },
      },
      {
        id: 'e_ans',
        source: { nodeId: 'c', portName: 'answers' },
        target: { nodeId: 'asker', portName: '__clarify_response__' },
      },
    ],
  }
}

describe('RFC-092 S-1 端到端 — mid-run clarify 答题由活调度循环自取（不再 scheduler stalled）', () => {
  let h: Harness
  beforeEach(async () => {
    h = await buildHarness()
  })
  afterEach(() => h.cleanup())

  test('slow sibling 仍在跑时答题（autoDispatchClarifyRound）→ 任务 done、rerun 行被自取且 prompt 带答案、slow 恰跑一次', async () => {
    await seedAgent(h.db, 'asker', ['design'])
    await seedAgent(h.db, 'slow', ['out'])
    // asker：第 1 次发 clarify、第 2 次（rerun）出正常 envelope；slow：gate 放行后出 envelope。
    writeFileSync(
      h.planFile,
      JSON.stringify({
        asker: [{ clarify: CLARIFY_BODY }, { output: { design: 'design-after-answer' } }],
        slow: [{ output: { out: 'slow-done' } }],
      }),
    )
    const taskId = await seedWorkflowAndTask(h, diamondDef(), { req: 'go' })

    const runPromise = runTask({
      taskId,
      db: h.db,
      appHome: h.appHome,
      opencodeCmd: ['bun', 'run', h.mockPath],
    })

    let rerunNodeRunId = ''
    try {
      // 等到「clarify 会话已停泊 ∧ slow 仍 running」——这是 S-1 的提交窗口。
      const session = await waitFor(async () => {
        const s = (
          await h.db
            .select()
            .from(clarifySessions)
            .where(
              and(eq(clarifySessions.taskId, taskId), eq(clarifySessions.status, 'awaiting_human')),
            )
        )[0]
        if (s === undefined) return undefined
        const slowRuns = await topLevelRuns(h.db, taskId, 'slow')
        if (slowRuns.length !== 1 || slowRuns[0]!.status !== 'running') return undefined
        return s
      }, 'open clarify session while slow sibling is still running')

      // 窗口证据：任务整体仍 running（不是停泊后再答的 resume 路径）。
      const midTask = (await h.db.select().from(tasks).where(eq(tasks.id, taskId)))[0]!
      expect(midTask.status).toBe('running')

      // 真实入口提交答案（内部自带 RFC-058 clarify_rounds 双表镜像 + rerun 铸行）。
      const res = await autoDispatchClarifyRound({
        db: h.db,
        originNodeRunId: session.clarifyNodeRunId,
        answers: [CLARIFY_ANSWER],
        directive: 'stop', // RFC-100: finalize round → the rerun's <workflow-output> is accepted
        actor,
      })
      rerunNodeRunId = res.dispatch.reruns[0]!.nodeRunId

      // 提交瞬间 rerun 行是 pending、slow 仍在跑——确认答案确实落在窗口内。
      const rerunRow = (
        await h.db.select().from(nodeRuns).where(eq(nodeRuns.id, rerunNodeRunId))
      )[0]!
      expect(rerunRow.status).toBe('pending')
      const slowAtSubmit = (await topLevelRuns(h.db, taskId, 'slow'))[0]!
      expect(slowAtSubmit.status).toBe('running')
    } finally {
      // 无论上面成败都放行 slow，避免 mock 等满 20s 才退出。
      writeFileSync(h.gateFile, 'go\n')
      await runPromise.catch(() => {})
    }
    await runPromise

    // 正确语义（RFC-092 §1.2）：活调度循环在 slow settle 后的 tick 经
    // pending-bypass 自取 rerun 行 → 任务 done，绝不是 'scheduler stalled' 假失败。
    const taskRow = (await h.db.select().from(tasks).where(eq(tasks.id, taskId)))[0]!
    expect(taskRow.errorMessage ?? '').not.toContain('scheduler stalled')
    expect(taskRow.status).toBe('done')

    // rerun 行（统一 dispatch 铸的那一行，被 runOneNode pendingExisting
    // 复用）跑到 done，prompt 注入了 Q&A。
    const rerun = (await h.db.select().from(nodeRuns).where(eq(nodeRuns.id, rerunNodeRunId)))[0]!
    expect(rerun.status).toBe('done')
    expect(rerun.promptText ?? '').toContain('Clarify Q&A')
    expect(rerun.promptText ?? '').toContain('Postgres')

    // asker 顶层恰两行：发问的 done + rerun done（无多余铸行）。
    const askerRuns = await topLevelRuns(h.db, taskId, 'asker')
    expect(askerRuns.length).toBe(2)
    expect(askerRuns.every((r) => r.status === 'done')).toBe(true)

    // mock 落盘 argv：asker 恰被调起两次，第二次（rerun）的 prompt 含答案文本。
    const spawns = readSpawns(h.stateDir)
    const askerSpawns = spawns
      .filter((s) => s.agent === 'asker')
      .sort((a, b) => a.callIndex - b.callIndex)
    expect(askerSpawns.length).toBe(2)
    expect(askerSpawns[1]!.prompt).toContain('Clarify Q&A')
    expect(askerSpawns[1]!.prompt).toContain('Postgres')

    // slow 不受扰：恰好一次 spawn、一行 done。
    expect(spawns.filter((s) => s.agent === 'slow').length).toBe(1)
    const slowRuns = await topLevelRuns(h.db, taskId, 'slow')
    expect(slowRuns.length).toBe(1)
    expect(slowRuns[0]!.status).toBe('done')

    // 会话闭环：answered（不再有 open session）。
    const sessions = await h.db
      .select()
      .from(clarifySessions)
      .where(eq(clarifySessions.taskId, taskId))
    expect(sessions.length).toBe(1)
    expect(sessions[0]!.status).toBe('answered')
  }, 20_000)
})
