import { rimrafDir } from './helpers/cleanup'
// RFC-097 — resume/retry 任务级互斥竞态 oracle（design/RFC-097-task-status-cas/design.md §3 / §7.3）。
//
// 锁定的合同（任一回归即红）：
//   1. 真并发双 resumeTask：pending CAS 前移为所有权锁（task.ts resumeTask），恰一个
//      赢者继续回滚 + kick；输者 409 code='task-not-resumable'，零副作用——node_runs
//      不出现双份新行、agent 只被派发一次、任务收敛到单一终态。
//   2. retryNode 同型：CAS 翻 pending 前移到回滚与铸占位行之前——输者不再向
//      node_runs 污染 'queued for retry' 占位行（旧顺序下双 retry 会铸两份）。
//   3. 残余语义（设计 §3 明示不修、本文件锁定意图）：CAS 单胜只保证同一状态纪元。
//      胜者 kick 的 runTask 速败（任务回到 failed）之后，第二次 resume 看到
//      from=failed 合法再胜——等价于用户连按两次 Resume，无双写者，是允许的。
//   4. isTaskActive 入口拒（audit S-8）：调度器在跑（activeTasks 持有 controller）时
//      resumeTask → 409 task-not-resumable（message 注明 actively running，证明是
//      入口闸而非状态门）；retryNode → 409 task-still-running。
//
// 确定性论证：双 resume 在同一 tick 内发起；赢者的 CAS 落账后立即进入真实 git
// 回滚（预置真实 stash sha → reset/clean/stash-apply 三次子进程 await），输者在任何
// 子进程完成之前就走完自己的 getTask + 状态门/CAS——要么 fast-path 读到 pending 409，
// 要么 CAS affected=0 ConcurrentTaskTransition 409，两条路映射同一 code。

